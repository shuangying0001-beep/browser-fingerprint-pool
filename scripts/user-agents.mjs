export const allUserAgents = [
  ...chromeWindows, ...chromeMac, ...firefox, ...edge, ...safari, ...opera, ...chromeLinux,
];

export function getRandomUserAgent() {
  return allUserAgents[Math.floor(Math.random() * allUserAgents.length)];
}

export function getUserAgentByType(type) {
  switch (type) {
    case 'chrome': {
      const chromeAll = [...chromeWindows, ...chromeMac, ...chromeLinux];
      return chromeAll[Math.floor(Math.random() * chromeAll.length)];
    }
    case 'firefox': return firefox[Math.floor(Math.random() * firefox.length)];
    case 'edge': return edge[Math.floor(Math.random() * edge.length)];
    case 'safari': return safari[Math.floor(Math.random() * safari.length)];
    case 'opera': return opera[Math.floor(Math.random() * opera.length)];
    default: return getRandomUserAgent();
  }
}

export default { allUserAgents, getRandomUserAgent, getUserAgentByType };
